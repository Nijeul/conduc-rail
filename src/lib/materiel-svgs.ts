  const d = designation.toLowerCase()
  const t = type.toLowerCase()

  if (d.includes('ballastière') || d.includes('ballastiere') ||
      d.includes('d12') || d.includes('c12') || d.includes('ex 100') || d.includes('ex100'))
    return MATERIEL_SVGS['Ballastiere'] ?? null

  if (d.includes('bigrue') || d.includes('dgs82'))
    return MATERIEL_SVGS['Bigrue'] ?? null

  if (t === 'bml' || d.includes('bml') || d.includes('108-'))
    return MATERIEL_SVGS['BML'] ?? null

  if (t === 'regaleuse' || d.includes('régaleuse') || d.includes('regaleuse') ||
      d.includes('ssp') || d.includes('r24 e'))
    return MATERIEL_SVGS['Regaleuse'] ?? null

  if (t === 'stabilisateur' || d.includes('stabilisateur') || d.includes('dgs42') || d.includes('dgs72'))
    return MATERIEL_SVGS['Stabilisateur'] ?? null

  if (d.includes('traverse'))
    return MATERIEL_SVGS['Wagon_traverses'] ?? null

  if (d.includes('pupitre'))
    return MATERIEL_SVGS['Wagon_pupitre'] ?? null

  if ((d.includes('ballast') || d.includes('appro')) && (t === 'wagon' || t === 'wagonlrs'))
    return MATERIEL_SVGS['Wagon_ballast'] ?? null

  if (d.includes('lrs') || d.includes('goulotte') || d.includes('pousseur') ||
      d.includes('s03') || t === 'wagonlrs')
    return MATERIEL_SVGS['Wagon_lrs'] ?? null

  if (d.includes('rail') || d.includes('60kg') || d.includes('50kg') || d.includes('jic'))
    return MATERIEL_SVGS['Wagon_rails'] ?? null

  switch (t) {
    case 'loco':          return MATERIEL_SVGS['Loco'] ?? null
    case 'ballastiere':   return MATERIEL_SVGS['Ballastiere'] ?? null
    case 'bigrue':        return MATERIEL_SVGS['Bigrue'] ?? null
    case 'bml':           return MATERIEL_SVGS['BML'] ?? null
    case 'regaleuse':     return MATERIEL_SVGS['Regaleuse'] ?? null
    case 'stabilisateur': return MATERIEL_SVGS['Stabilisateur'] ?? null
    case 'wagon':         return MATERIEL_SVGS['Wagon_vide'] ?? null
    case 'wagonlrs':      return MATERIEL_SVGS['Wagon_lrs'] ?? null
    default:              return MATERIEL_SVGS['Wagon_vide'] ?? null
  }
